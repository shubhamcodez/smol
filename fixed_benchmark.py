"""Fixed benchmark for the local self-improving loop.

Do not edit this file during a search run. It owns the deterministic task,
wall-clock budget, quality metric, and deployment timing gate.
"""

from __future__ import annotations

import argparse
import json
import math
import time
from pathlib import Path
from typing import Any

import numpy as np

from backend import detect_runtime


INPUT_DIM = 64
OUTPUT_DIM = 16
TRAIN_ROWS = 4096
VALID_ROWS = 1024
DATA_SEED = 8142
STATIC_BATCH = 32


def validate_candidate(candidate: dict[str, Any]) -> None:
    required = {
        "hidden_dim",
        "learning_rate",
        "weight_decay",
        "batch_size",
        "beta1",
        "beta2",
        "seed",
    }
    missing = required - candidate.keys()
    if missing:
        raise ValueError(f"candidate is missing keys: {sorted(missing)}")
    if candidate["hidden_dim"] not in {32, 48, 64, 96, 128, 160, 192, 256}:
        raise ValueError("hidden_dim is outside the fixed search space")
    if candidate["batch_size"] not in {32, 64, 128, 256}:
        raise ValueError("batch_size is outside the fixed search space")
    if not 1e-5 <= candidate["learning_rate"] <= 0.05:
        raise ValueError("learning_rate is outside the fixed search space")
    if not 0.0 <= candidate["weight_decay"] <= 0.2:
        raise ValueError("weight_decay is outside the fixed search space")
    if not 0.0 < candidate["beta1"] < 1.0 or not 0.0 < candidate["beta2"] < 1.0:
        raise ValueError("Adam betas must be between zero and one")


def make_dataset() -> tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    """A fixed nonlinear teacher task; candidates never see validation labels."""
    rng = np.random.default_rng(DATA_SEED)
    rows = TRAIN_ROWS + VALID_ROWS
    x = rng.normal(size=(rows, INPUT_DIM)).astype(np.float32)
    teacher_w1 = rng.normal(0.0, 0.22, (INPUT_DIM, 128)).astype(np.float32)
    teacher_b1 = rng.normal(0.0, 0.08, 128).astype(np.float32)
    teacher_w2 = rng.normal(0.0, 0.18, (128, OUTPUT_DIM)).astype(np.float32)
    logits = np.maximum(x @ teacher_w1 + teacher_b1, 0.0) @ teacher_w2
    logits += rng.normal(0.0, 0.08, logits.shape).astype(np.float32)
    y = logits.argmax(axis=1).astype(np.int64)
    return x[:TRAIN_ROWS], y[:TRAIN_ROWS], x[TRAIN_ROWS:], y[TRAIN_ROWS:]


def cross_entropy(logits: np.ndarray, targets: np.ndarray) -> float:
    shifted = logits - logits.max(axis=1, keepdims=True)
    log_sum_exp = np.log(np.exp(shifted).sum(axis=1))
    return float(np.mean(log_sum_exp - shifted[np.arange(len(targets)), targets]))


def initialize_weights(candidate: dict[str, Any]) -> dict[str, np.ndarray]:
    rng = np.random.default_rng(int(candidate["seed"]))
    hidden = int(candidate["hidden_dim"])
    return {
        "w1": rng.normal(0.0, math.sqrt(2.0 / INPUT_DIM), (INPUT_DIM, hidden)).astype(np.float32),
        "b1": np.zeros(hidden, dtype=np.float32),
        "w2": rng.normal(0.0, math.sqrt(2.0 / hidden), (hidden, OUTPUT_DIM)).astype(np.float32),
        "b2": np.zeros(OUTPUT_DIM, dtype=np.float32),
    }


def train_numpy(
    candidate: dict[str, Any],
    x_train: np.ndarray,
    y_train: np.ndarray,
    budget_seconds: float,
) -> tuple[dict[str, np.ndarray], int, float]:
    weights = initialize_weights(candidate)
    first = {name: np.zeros_like(value) for name, value in weights.items()}
    second = {name: np.zeros_like(value) for name, value in weights.items()}
    rng = np.random.default_rng(int(candidate["seed"]) + 1)
    lr = float(candidate["learning_rate"])
    decay = float(candidate["weight_decay"])
    beta1 = float(candidate["beta1"])
    beta2 = float(candidate["beta2"])
    batch_size = int(candidate["batch_size"])
    start = time.perf_counter()
    step = 0

    while step < 20 or time.perf_counter() - start < budget_seconds:
        step += 1
        indices = rng.integers(0, len(x_train), size=batch_size)
        x = x_train[indices]
        targets = y_train[indices]
        hidden_pre = x @ weights["w1"] + weights["b1"]
        hidden = np.maximum(hidden_pre, 0.0)
        logits = hidden @ weights["w2"] + weights["b2"]
        logits -= logits.max(axis=1, keepdims=True)
        probabilities = np.exp(logits)
        probabilities /= probabilities.sum(axis=1, keepdims=True)
        probabilities[np.arange(batch_size), targets] -= 1.0
        probabilities /= batch_size

        gradients = {
            "w2": hidden.T @ probabilities,
            "b2": probabilities.sum(axis=0),
        }
        hidden_gradient = probabilities @ weights["w2"].T
        hidden_gradient[hidden_pre <= 0.0] = 0.0
        gradients["w1"] = x.T @ hidden_gradient
        gradients["b1"] = hidden_gradient.sum(axis=0)

        for name in weights:
            gradient = gradients[name]
            if name.startswith("w"):
                gradient = gradient + decay * weights[name]
            first[name] = beta1 * first[name] + (1.0 - beta1) * gradient
            second[name] = beta2 * second[name] + (1.0 - beta2) * (gradient * gradient)
            first_hat = first[name] / (1.0 - beta1**step)
            second_hat = second[name] / (1.0 - beta2**step)
            weights[name] -= lr * first_hat / (np.sqrt(second_hat) + 1e-8)

    return weights, step, time.perf_counter() - start


def train_cuda(
    candidate: dict[str, Any],
    x_train: np.ndarray,
    y_train: np.ndarray,
    budget_seconds: float,
) -> tuple[dict[str, np.ndarray], int, float]:
    import torch

    torch.manual_seed(int(candidate["seed"]))
    device = torch.device("cuda")
    model = torch.nn.Sequential(
        torch.nn.Linear(INPUT_DIM, int(candidate["hidden_dim"])),
        torch.nn.ReLU(),
        torch.nn.Linear(int(candidate["hidden_dim"]), OUTPUT_DIM),
    ).to(device)
    optimizer = torch.optim.AdamW(
        model.parameters(),
        lr=float(candidate["learning_rate"]),
        weight_decay=float(candidate["weight_decay"]),
        betas=(float(candidate["beta1"]), float(candidate["beta2"])),
    )
    features = torch.from_numpy(x_train).to(device)
    labels = torch.from_numpy(y_train).to(device)
    generator = torch.Generator(device=device).manual_seed(int(candidate["seed"]) + 1)
    batch_size = int(candidate["batch_size"])
    torch.cuda.synchronize()
    start = time.perf_counter()
    step = 0
    while step < 20 or time.perf_counter() - start < budget_seconds:
        step += 1
        indices = torch.randint(len(features), (batch_size,), generator=generator, device=device)
        loss = torch.nn.functional.cross_entropy(model(features[indices]), labels[indices])
        optimizer.zero_grad(set_to_none=True)
        loss.backward()
        optimizer.step()
        if step % 10 == 0:
            torch.cuda.synchronize()
    torch.cuda.synchronize()
    elapsed = time.perf_counter() - start
    first, second = model[0], model[2]
    weights = {
        "w1": first.weight.detach().float().cpu().numpy().T.copy(),
        "b1": first.bias.detach().float().cpu().numpy().copy(),
        "w2": second.weight.detach().float().cpu().numpy().T.copy(),
        "b2": second.bias.detach().float().cpu().numpy().copy(),
    }
    return weights, step, elapsed


def benchmark_cpu(weights: dict[str, np.ndarray], sample: np.ndarray) -> dict[str, Any]:
    def forward():
        return np.maximum(sample @ weights["w1"] + weights["b1"], 0.0) @ weights["w2"] + weights["b2"]

    for _ in range(20):
        output = forward()
    start = time.perf_counter()
    for _ in range(200):
        output = forward()
    elapsed_ms = (time.perf_counter() - start) * 1000.0 / 200.0
    return {
        "backend": "numpy-cpu",
        "device": {
            "execution_provider": "NumPy CPU",
            "hardware_type": "CPU",
        },
        "latency_ms": elapsed_ms,
        "iterations": 200,
        "output_shape": list(output.shape),
        "output_checksum": float(output.sum()),
    }


def benchmark_cuda(weights: dict[str, np.ndarray], sample: np.ndarray) -> dict[str, Any]:
    import torch

    x = torch.from_numpy(sample).cuda()
    w1 = torch.from_numpy(weights["w1"]).cuda()
    b1 = torch.from_numpy(weights["b1"]).cuda()
    w2 = torch.from_numpy(weights["w2"]).cuda()
    b2 = torch.from_numpy(weights["b2"]).cuda()

    def forward():
        return torch.relu(x @ w1 + b1) @ w2 + b2

    for _ in range(20):
        output = forward()
    torch.cuda.synchronize()
    start = torch.cuda.Event(enable_timing=True)
    end = torch.cuda.Event(enable_timing=True)
    start.record()
    for _ in range(200):
        output = forward()
    end.record()
    torch.cuda.synchronize()
    return {
        "backend": "torch-cuda",
        "device": {
            "execution_provider": "PyTorch CUDA",
            "hardware_type": "GPU",
            "vendor": "NVIDIA",
            "name": torch.cuda.get_device_name(0),
        },
        "latency_ms": start.elapsed_time(end) / 200.0,
        "iterations": 200,
        "output_shape": list(output.shape),
        "output_checksum": float(output.float().sum().item()),
        "torch_version": torch.__version__,
    }


def choose_backends(requested_training: str, requested_deployment: str) -> tuple[str, str]:
    inventory = detect_runtime()
    training = requested_training
    if training == "auto":
        training = "cuda" if inventory.cuda else "cpu"
    if training == "cuda" and not inventory.cuda:
        raise RuntimeError("CUDA training was required but torch.cuda.is_available() is false")

    deployment = requested_deployment
    if deployment == "auto":
        deployment = "cuda" if inventory.cuda else "cpu"
    if deployment == "cuda" and not inventory.cuda:
        raise RuntimeError("CUDA deployment was required but torch.cuda.is_available() is false")
    return training, deployment


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--candidate", type=Path, required=True)
    parser.add_argument("--run-id", required=True)
    parser.add_argument("--artifacts", type=Path, required=True)
    parser.add_argument("--budget-seconds", type=float, default=1.0)
    parser.add_argument("--training-backend", choices=["auto", "cpu", "cuda"], default="auto")
    parser.add_argument("--deployment", choices=["auto", "cuda", "cpu"], default="auto")
    args = parser.parse_args()

    candidate = json.loads(args.candidate.read_text(encoding="utf-8"))
    validate_candidate(candidate)
    training_backend, deployment_backend = choose_backends(args.training_backend, args.deployment)
    x_train, y_train, x_valid, y_valid = make_dataset()

    if training_backend == "cuda":
        weights, steps, train_seconds = train_cuda(candidate, x_train, y_train, args.budget_seconds)
    else:
        weights, steps, train_seconds = train_numpy(candidate, x_train, y_train, args.budget_seconds)

    valid_logits = np.maximum(x_valid @ weights["w1"] + weights["b1"], 0.0) @ weights["w2"] + weights["b2"]
    validation_loss = cross_entropy(valid_logits, y_valid)
    run_dir = args.artifacts / args.run_id
    run_dir.mkdir(parents=True, exist_ok=True)
    sample = x_valid[:STATIC_BATCH]
    if deployment_backend == "cuda":
        deployment = benchmark_cuda(weights, sample)
    else:
        deployment = benchmark_cpu(weights, sample)

    parameter_count = sum(value.size for value in weights.values())
    score = (
        validation_loss
        + 0.002 * math.log1p(float(deployment["latency_ms"]))
        + 1e-7 * parameter_count
    )
    metrics = {
        "run_id": args.run_id,
        "score": score,
        "validation_loss": validation_loss,
        "parameter_count": parameter_count,
        "training_backend": training_backend,
        "training_seconds": train_seconds,
        "training_steps": steps,
        "deployment": deployment,
        "candidate": candidate,
        "runtime_inventory": detect_runtime().to_dict(),
    }
    metrics_path = run_dir / "metrics.json"
    metrics_path.write_text(json.dumps(metrics, indent=2) + "\n", encoding="utf-8")
    print("AUTORESEARCH_METRICS " + json.dumps(metrics, separators=(",", ":")))


if __name__ == "__main__":
    main()
